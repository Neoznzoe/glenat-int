export function DocFileIcon({ extension, size = 20 }: DocFileIconProps) {
  const ext = (extension || '').toLowerCase().replace('.', '');
  const color = getExtensionColor(extension);

  switch (ext) {
    case 'pdf':
      return <PdfIcon color={color} size={size} />;
    case 'doc':
    case 'docx':
      return <WordIcon color={color} size={size} />;
    case 'jpg':
    case 'jpeg':
    case 'png':
    case 'gif':
      return <ImageIcon color={color} size={size} />;
    case 'ppt':
    case 'pptx':
      return <SlidesIcon color={color} size={size} />;
    case 'xls':
    case 'xlsx':
      return <ExcelIcon color={color} size={size} />;
    default:
      return <GenericFileIcon color={color} size={size} />;
  }
}
